def main():
    # Define the radius of the circle
    radius = 20

    # Define PI as 3.14159
    from math import pi

    # Compute area
    area = pi * radius ** 2

    # Display results
    print("The area for the circle of radius", radius, "is", area) # Output: The area for the circle of radius 20 is 1256.6370614359173


if __name__ == "__main__":
    main() 